#!/usr/bin/env node
/**
 * Third-party notices generator (CH-1, Master Patch #5).
 *
 * Reads `pnpm licenses list --json --prod` and emits THIRD_PARTY_NOTICES.md at
 * the repo root: one section per license (packages sorted), plus two explicit
 * compliance sections —
 *   - "LGPL-3.0 (dynamically linked)": names the @img/sharp-libvips-* native
 *     libraries we ship as prebuilt shared objects (dynamic linking, no
 *     relicensing obligation).
 *   - "MPL-2.0": file-level copyleft dependencies.
 *
 *   node scripts/generate-third-party-notices.mjs
 */
import { execFileSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(REPO_ROOT, 'THIRD_PARTY_NOTICES.md');

function loadLicenses() {
  let raw;
  try {
    raw = execFileSync('pnpm', ['licenses', 'list', '--json', '--prod'], {
      encoding: 'utf8',
      maxBuffer: 64 * 1024 * 1024,
    });
  } catch (err) {
    if (err.stdout) raw = err.stdout.toString();
    else throw err;
  }
  return JSON.parse(raw);
}

function pkgLine(pkg) {
  const versions = Array.isArray(pkg.versions) ? pkg.versions : [pkg.version].filter(Boolean);
  const version = versions.join(', ') || 'unknown';
  const author = pkg.author ? ` — ${pkg.author}` : '';
  return `- \`${pkg.name}@${version}\`${author}`;
}

function main() {
  const data = loadLicenses();

  const lines = [];
  lines.push('# Third-Party Notices');
  lines.push('');
  lines.push(
    'This product includes the following third-party open-source software, ' +
      'grouped by license. Generated by `scripts/generate-third-party-notices.mjs` ' +
      'from the production dependency tree (`pnpm licenses list --prod`).',
  );
  lines.push('');
  lines.push(`_Last generated: ${new Date().toISOString().slice(0, 10)}._`);
  lines.push('');

  // ── Dynamically-linked LGPL section (sharp / libvips) ─────────────────────
  const lgplPkgs = [];
  const mplPkgs = [];
  for (const [license, pkgs] of Object.entries(data)) {
    for (const pkg of pkgs) {
      if (/LGPL/i.test(license)) lgplPkgs.push(pkg);
      if (/^MPL-2\.0/i.test(license)) mplPkgs.push(pkg);
    }
  }

  lines.push('## LGPL-3.0 (dynamically linked)');
  lines.push('');
  lines.push(
    'The following components are licensed under the LGPL and are consumed as ' +
      '**dynamically linked** prebuilt shared libraries (native `.so`/`.dylib` ' +
      'binaries loaded at runtime by `sharp`). We do not modify or statically ' +
      'link them; the LGPL permits this without relicensing our source. Source ' +
      'for these libraries is available from their respective upstreams.',
  );
  lines.push('');
  const sharpLibvips = lgplPkgs
    .filter((p) => /@img\/sharp-libvips/.test(p.name))
    .sort((a, b) => a.name.localeCompare(b.name));
  const otherLgpl = lgplPkgs
    .filter((p) => !/@img\/sharp-libvips/.test(p.name))
    .sort((a, b) => a.name.localeCompare(b.name));
  if (sharpLibvips.length === 0) {
    lines.push('- _(no `@img/sharp-libvips-*` packages resolved in this build)_');
  } else {
    for (const p of sharpLibvips) lines.push(pkgLine(p));
  }
  for (const p of otherLgpl) lines.push(pkgLine(p));
  lines.push('');

  // ── MPL-2.0 section ───────────────────────────────────────────────────────
  lines.push('## MPL-2.0');
  lines.push('');
  lines.push(
    'The following components are licensed under the Mozilla Public License 2.0 ' +
      '(file-level copyleft). Source is available from their upstream repositories.',
  );
  lines.push('');
  if (mplPkgs.length === 0) {
    lines.push('- _(no MPL-2.0 packages resolved in this build)_');
  } else {
    for (const p of [...mplPkgs].sort((a, b) => a.name.localeCompare(b.name))) {
      lines.push(pkgLine(p));
    }
  }
  lines.push('');

  // ── All licenses, grouped ─────────────────────────────────────────────────
  lines.push('## All dependencies by license');
  lines.push('');
  const sortedLicenses = Object.keys(data).sort((a, b) => a.localeCompare(b));
  for (const license of sortedLicenses) {
    const pkgs = [...data[license]].sort((a, b) => a.name.localeCompare(b.name));
    lines.push(`### ${license}`);
    lines.push('');
    for (const p of pkgs) lines.push(pkgLine(p));
    lines.push('');
  }

  writeFileSync(OUT, lines.join('\n'));
  const count = Object.values(data).reduce((n, arr) => n + arr.length, 0);
  console.log(`generate-third-party-notices: wrote ${OUT} (${count} packages, ${sortedLicenses.length} licenses).`);
}

main();
